namespace KosHome.Domain.Common.Pagination;

/// <summary>
/// Represents the direction for cursor-based pagination.
/// </summary>
public enum PaginationDirection
{
    /// <summary>
    /// Forward pagination (next page).
    /// </summary>
    Forward = 0,

    /// <summary>
    /// Backward pagination (previous page).
    /// </summary>
    Backward = 1
}

